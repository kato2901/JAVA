public class Truefalse {
    public static void main(String[] args) {
      // 「真」を表す真偽値を出力
        System.out.println(true);

      // 「偽」を表す真偽値を出力
        System.out.println(false);

      // 「==」を用いて、値を比較した結果を出力
        System.out.println(12 / 4 == 3);

      // 「!=」を用いて、値を比較した結果を出力
        System.out.println(12 / 4 != 3);

      // boolean型の変数boolを定義し、「3 * 9 == 27」を代入
      boolean bool = 3 * 9 == 27;

      // 変数boolの値を出力
        System.out.println(bool);
    }
}